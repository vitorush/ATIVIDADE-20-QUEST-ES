semana = int(input("Digite um número de um a sete:"))
if semana ==1:
    print("Domingo")
elif semana ==2:
    print("Segunda")
elif semana ==3:
    print("Terça")
elif semana ==4:
    print("Quarta")
elif semana ==5:
    print("Quinta")
elif semana ==6:
    print("Sexta")
else:
    print("Sábado")